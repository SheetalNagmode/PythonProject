# Iterative Statements:
# While Loop
# Syntax:
# i=1;
# while condition:
#       statements;
#       i=i+1

# Incrementation Example:

i=1;
while i<=10:
    print(i)
    i=i+1

# if range is known use "for loop" otherwise use "while loop"
# Decrementation Example:

i=10;
while i>=1:
    print(i)
    i=i-1