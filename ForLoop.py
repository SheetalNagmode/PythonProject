#Iterative Statements:
# for loop
# single parameter/argument

for i in range(10):
    print(i)

# multiple parameters/arguments
# incrementation Example:

for i in range(2,10,2):         # will print even numbers
    print(i)                    #(2,4,6,8)

for i in range(1,10,2):         # will print Odd numbers
    print(i)                    # (1,3,5,7,9)

# Decremental Example:

for i in range(10, 1, -1):
    print(i)                    # (10,9,8,7,6,5,4,3,2)