# Creating Tuple:
# Tuples is similar to list but its immutable
# No add,delete,replace,reorder of elements is allowed once created.
# () are used to define tuple
# Ways to create tuples:

t=()                       #Creates an empty tuple with no data
t1 = (10,11,12)            #tuple of numbers
t2 = tuple([1,2,3,4,5])    #tuple from array/list
t3 = tuple("Sam")          #tuple from string/
#takes every character in the string as different variables

print(t)
print(t1)
print(t2)
print(t3)